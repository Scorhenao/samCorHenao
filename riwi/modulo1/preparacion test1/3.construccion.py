#Problema: Cálculo de Materiales para la Construcción de una Pared



#Descripción del problema:

#Un contratista de construcción necesita calcular la cantidad de materiales necesarios para construir una pared rectangular en un proyecto de construcción. La pared tendrá una longitud y altura especificadas por el usuario, y se utilizarán ladrillos estándar para su construcción. El contratista necesita determinar la cantidad de ladrillos necesarios, así como la cantidad de mortero requerida para unir los ladrillos.


'''
Requerimientos:



El programa debe solicitar al usuario que ingrese la longitud (en metros) y la altura (en metros) de la pared.



El programa debe calcular el área de la pared utilizando la fórmula del área de un rectángulo: área = longitud * altura.



El programa debe calcular la cantidad de ladrillos necesarios para construir la pared. Se asume que se utiliza un solo tipo de ladrillo estándar, y cada ladrillo tiene dimensiones conocidas (por ejemplo, 0.2 metros de largo, 0.1 metros de ancho y 0.05 metros de alto). El programa debe calcular la cantidad total de ladrillos necesarios considerando el espacio perdido debido a la disposición de los ladrillos.



El programa debe calcular la cantidad de mortero necesaria para unir los ladrillos. Se asume que se utiliza una cierta cantidad de mortero por metro cuadrado de área de la pared, y el usuario debe proporcionar esta cantidad.



El programa debe mostrar al usuario la cantidad total de ladrillos necesarios y la cantidad de mortero requerida para construir la pared.



El programa debe manejar adecuadamente las entradas del usuario y validar que las dimensiones ingresadas sean valores numéricos positivos.
'''



