#elabore un algoritmo que capture el radio de un circulo y calcule su perimetro
radio = float(input("ingrese el radio del circulo: "))
py = 3.1416
perimetro = 2*py*radio
print("el perimetro de su circulo es de: ", perimetro)
print(f"el perimetro de su circulo es:{perimetro} ") #otra manera de usar print
