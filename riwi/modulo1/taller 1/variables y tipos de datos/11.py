#ejercicio 11: Calcular el promedio de una lista de números.

# Definir la lista de números
numeros = [4, 6, 8, 10, 12]

# Calcular la suma de los números en la lista
suma_n = sum(numeros)

# Calcular la cantidad total de números en la lista
total_numeros = len(numeros)
#len = longitud todal de la cual se tomaran numeros


# Calcular el promedio
promedio = suma_n / total_numeros

# Mostrar el promedio
print("El promedio de la lista de números es:", promedio)